package zookeeper;

public class Monkey extends Primate {
    public void climb() {
        System.out.println("The monkey climbs.");
    }

    public void hoot() {
        System.out.println("The monkey hoots.");
    }

    public void howl() {
        System.out.println("The monkey howls.");
    }

    @Override
    public void say() {
        howl();
    }
}
